/* The mailbox buffer MUST be 16-byte aligned and in the lowest 1 GB
 * (VC bus address range).  We use .bss which sits in low memory. */
static volatile uint32_t __attribute__((aligned(16))) mbox_buf[36];

int fb_init(void)
{
    fb.ready = 0;

    /* Desired resolution — try 1280×720 for broad HDMI compatibility */
    uint32_t want_w = 1280;
    uint32_t want_h = 720;
    uint32_t want_depth = 32;

    /* Build mailbox property request */
    uint32_t idx = 0;
    mbox_buf[idx++] = 0;              /* [0] total size (fill later) */
    mbox_buf[idx++] = MBOX_REQUEST;   /* [1] request code */

    /* Tag: set physical display size */
    mbox_buf[idx++] = TAG_SET_PHYS_WH;
    mbox_buf[idx++] = 8;              /* value buffer size */
    mbox_buf[idx++] = 0;              /* request indicator */
    mbox_buf[idx++] = want_w;         /* width */
    mbox_buf[idx++] = want_h;         /* height */

    /* Tag: set virtual display size (same as physical, no scrolling) */
    mbox_buf[idx++] = TAG_SET_VIRT_WH;
    mbox_buf[idx++] = 8;
    mbox_buf[idx++] = 0;
    mbox_buf[idx++] = want_w;
    mbox_buf[idx++] = want_h;

    /* Tag: set depth */
    mbox_buf[idx++] = TAG_SET_DEPTH;
    mbox_buf[idx++] = 4;
    mbox_buf[idx++] = 0;
    mbox_buf[idx++] = want_depth;

    /* Tag: set pixel order (1 = RGB) */
    mbox_buf[idx++] = TAG_SET_PIXEL_ORDER;
    mbox_buf[idx++] = 4;
    mbox_buf[idx++] = 0;
    mbox_buf[idx++] = 1;              /* RGB */

    /* Tag: allocate buffer */
    mbox_buf[idx++] = TAG_ALLOC_BUFFER;
    mbox_buf[idx++] = 8;
    mbox_buf[idx++] = 0;
    mbox_buf[idx++] = 4096;           /* alignment */
    mbox_buf[idx++] = 0;              /* will receive size */

    /* Tag: get pitch */
    mbox_buf[idx++] = TAG_GET_PITCH;
    mbox_buf[idx++] = 4;
    mbox_buf[idx++] = 0;
    mbox_buf[idx++] = 0;              /* will receive pitch */

    /* End tag */
    mbox_buf[idx++] = TAG_END;

    /* Fill total size */
    mbox_buf[0] = idx * 4;

    /* Flush CPU cache to SDRAM so the GPU sees our mailbox data.
     * The VideoCore reads from SDRAM, not the ARM L1/L2 cache. */
    uint32_t buf_size = idx * 4;
    arm_cache_clean_invalidate(mbox_buf, buf_size);

    /* Send the buffer address to mailbox channel 8.
     * The VC expects a BUS address.  On the BCM2711 the ARM→VC mapping
     * for the first 1 GB is: bus_addr = phys_addr | 0xC0000000.
     * The 0xC0000000 alias bypasses the VC's L2 cache for coherency. */
    uint32_t buf_phys = (uint32_t)(uintptr_t)mbox_buf;
    uint32_t bus_addr = buf_phys | 0xC0000000;
    mbox_write(MBOX_CH_PROP, bus_addr);

    /* Read response */
    (void)mbox_read(MBOX_CH_PROP);

    /* Invalidate cache so we read the GPU's response from SDRAM,
     * not stale cached data. */
    arm_cache_clean_invalidate(mbox_buf, buf_size);

    /* Check response */
    if (mbox_buf[1] != MBOX_RESPONSE) {
        return -1;  /* Mailbox error */
    }

    /* Parse the allocate-buffer tag response.
     * Find it by scanning — tag order in response matches request. */
    uint32_t fb_addr = 0, fb_size = 0, fb_pitch = 0;
    uint32_t i = 2;  /* skip header */
    while (i < idx - 1) {
        uint32_t tag = mbox_buf[i];
        uint32_t vsize = mbox_buf[i + 1];
        /* uint32_t indicator = mbox_buf[i + 2]; */
        if (tag == TAG_ALLOC_BUFFER) {
            fb_addr = mbox_buf[i + 3];
            fb_size = mbox_buf[i + 4];
        } else if (tag == TAG_GET_PITCH) {
            fb_pitch = mbox_buf[i + 3];
        } else if (tag == TAG_END) {
            break;
        }
        /* Advance: tag(4) + size(4) + indicator(4) + value_buffer(vsize, 4-aligned) */
        i += 3 + ((vsize + 3) / 4);
    }

    if (fb_addr == 0 || fb_size == 0 || fb_pitch == 0) {
        return -1;
    }

    /* Convert VC bus address to ARM physical address.
     * VC returns bus address with 0xC0000000 or 0x40000000 prefix.
     * Strip to get ARM physical address which our identity map covers. */
    fb_addr &= 0x3FFFFFFF;

    /* Populate state */
    fb.base   = (volatile uint32_t *)(uintptr_t)fb_addr;
    fb.width  = want_w;
    fb.height = want_h;
    fb.pitch  = fb_pitch;
    fb.depth  = want_depth;
    fb.size   = fb_size;

    /* Text console dimensions (8×16 font) */
    fb.cols     = fb.width / 8;
    fb.rows     = fb.height / 16;
    fb.cursor_x = 0;
    fb.cursor_y = 0;
    fb.fg_color = 0x0000FF00;  /* Bright green on black (hacker aesthetic) */
    fb.bg_color = 0x00000000;

    fb.ready = 1;

    /* Clear screen */
    fb_clear();

    return 0;
}

/* =============================================================================
 * Pixel-Level Drawing
 * =============================================================================*/

void fb_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color)
{
    if (!fb.ready) return;
    uint32_t pitch32 = fb.pitch / 4;  /* pitch in 32-bit pixels */
    for (uint32_t row = y; row < y + h && row < fb.height; row++) {
        volatile uint32_t *line = fb.base + row * pitch32 + x;
        for (uint32_t col = 0; col < w && (x + col) < fb.width; col++) {
            line[col] = color;
        }
    }
}

void fb_clear(void)
{
    if (!fb.ready) return;
    uint32_t total = fb.size / 4;
    for (uint32_t i = 0; i < total; i++)
        fb.base[i] = fb.bg_color;
    fb.cursor_x = 0;
    fb.cursor_y = 0;
}

/* =============================================================================
 * Character Drawing (8×16 bitmapped font)
 * =============================================================================*/

void fb_draw_char(uint32_t col, uint32_t row, char c, uint32_t fg, uint32_t bg)
{
    if (!fb.ready) return;
    uint32_t pitch32 = fb.pitch / 4;
    uint32_t px = col * 8;
    uint32_t py = row * 16;

    /* Clamp to printable range */
    int idx = (int)(unsigned char)c - 32;
    if (idx < 0 || idx > 94) idx = 0;  /* space for unprintable */

    const uint8_t *glyph = font8x16[idx];

    for (uint32_t y = 0; y < 16 && (py + y) < fb.height; y++) {
        volatile uint32_t *line = fb.base + (py + y) * pitch32 + px;
        uint8_t bits = glyph[y];
        for (uint32_t x = 0; x < 8 && (px + x) < fb.width; x++) {
            line[x] = (bits & 0x80) ? fg : bg;
            bits <<= 1;
        }
    }
}

/* =============================================================================
 * Console Scrolling
 * =============================================================================*/

static void fb_scroll_up(void)
{
    if (!fb.ready) return;
    uint32_t pitch32 = fb.pitch / 4;
    uint32_t row_pixels = 16;  /* pixels per text row */

    /* Copy rows 1..N-1 up by one text row */
    uint32_t copy_lines = (fb.rows - 1) * row_pixels;
    for (uint32_t y = 0; y < copy_lines; y++) {
        volatile uint32_t *dst = fb.base + y * pitch32;
        volatile uint32_t *src = fb.base + (y + row_pixels) * pitch32;
        for (uint32_t x = 0; x < fb.width; x++)
            dst[x] = src[x];
    }

    /* Clear the last text row */
    uint32_t last_start = copy_lines;
    for (uint32_t y = 0; y < row_pixels; y++) {
        volatile uint32_t *line = fb.base + (last_start + y) * pitch32;
        for (uint32_t x = 0; x < fb.width; x++)
            line[x] = fb.bg_color;
    }
}

/* =============================================================================
 * Console Character Output
 * =============================================================================*/

void fb_putchar(char c)
{
    if (!fb.ready) return;

    if (c == '\n') {
        fb.cursor_x = 0;
        fb.cursor_y++;
    } else if (c == '\r') {
        fb.cursor_x = 0;
    } else if (c == '\t') {
        fb.cursor_x = (fb.cursor_x + 4) & ~3;
    } else if (c == '\b') {
        if (fb.cursor_x > 0) {
            fb.cursor_x--;
            fb_draw_char(fb.cursor_x, fb.cursor_y, ' ', fb.fg_color, fb.bg_color);
        }
    } else {
        fb_draw_char(fb.cursor_x, fb.cursor_y, c, fb.fg_color, fb.bg_color);
        fb.cursor_x++;
    }

    /* Wrap at end of line */
    if (fb.cursor_x >= fb.cols) {
        fb.cursor_x = 0;
        fb.cursor_y++;
    }

    /* Scroll if past bottom */
    if (fb.cursor_y >= fb.rows) {
        fb_scroll_up();
        fb.cursor_y = fb.rows - 1;
    }
}

void fb_puts(const char *s)
{
    while (*s) {
        if (*s == '\n') fb_putchar('\r');
        fb_putchar(*s++);
    }
}

/* =============================================================================
 * Boot Splash — called after fb_init() to show TensorOS banner
 * =============================================================================*/

void fb_boot_splash(void)
{
    if (!fb.ready) return;

    /* Title bar: white on dark blue */
    uint32_t title_bg = 0x00002244;
    uint32_t title_fg = 0x00FFFFFF;

    fb_fill_rect(0, 0, fb.width, 32, title_bg);

    /* Draw title text at row 0, row 1 (16px each) */
    const char *title = " TensorOS v0.1.0 \"Neuron\" - Bare-Metal AI Operating System";
    uint32_t col = 0;
    for (const char *p = title; *p && col < fb.cols; p++, col++)
        fb_draw_char(col, 0, *p, title_fg, title_bg);

    const char *sub = " Raspberry Pi 4 (BCM2711 Cortex-A72) | ARM64 | HDMI 1280x720";
    col = 0;
    for (const char *p = sub; *p && col < fb.cols; p++, col++)
        fb_draw_char(col, 1, *p, 0x0088CCFF, title_bg);

    /* Horizontal separator */
    fb_fill_rect(0, 32, fb.width, 2, 0x00448866);

    /* Move cursor below splash (row 3 in text coordinates) */
    fb.cursor_y = 3;
    fb.cursor_x = 0;
}

