/* =============================================================================
 * VideoCore Mailbox Registers (BCM2711)
 * Use our own definitions to avoid conflicts with arm64_hal.h
 * =============================================================================*/

#define FB_MBOX_BASE    0xFE00B880ULL

#ifndef MBOX_READ
#define MBOX_READ       (FB_MBOX_BASE + 0x00)
#define MBOX_STATUS     (FB_MBOX_BASE + 0x18)
#define MBOX_WRITE      (FB_MBOX_BASE + 0x20)
#define MBOX_FULL       (1U << 31)
#define MBOX_EMPTY      (1U << 30)
#endif

#define MBOX_CH_PROP    8   /* ARM → VC property channel */

/* Property tag request/response codes */
#define MBOX_REQUEST    0x00000000
#define MBOX_RESPONSE   0x80000000

/* Framebuffer property tags */
#define TAG_SET_PHYS_WH     0x00048003
#define TAG_SET_VIRT_WH     0x00048004
#define TAG_SET_DEPTH       0x00048005
#define TAG_SET_PIXEL_ORDER 0x00048006
#define TAG_ALLOC_BUFFER    0x00040001
#define TAG_GET_PITCH       0x00040008
#define TAG_END             0x00000000

/* =============================================================================
 * Framebuffer Global State
 * =============================================================================*/

fb_state_t fb;

/* =============================================================================
 * Mailbox I/O
 * =============================================================================*/

static inline void mmio_write32(uint64_t addr, uint32_t val)
{
    *(volatile uint32_t *)(uintptr_t)addr = val;
}

static inline uint32_t mmio_read32(uint64_t addr)
{
    return *(volatile uint32_t *)(uintptr_t)addr;
}

/* Wait until mailbox is not full, then write value|channel */
static void mbox_write(uint8_t channel, uint32_t value)
{
    while (mmio_read32(MBOX_STATUS) & MBOX_FULL)
        __asm__ volatile ("nop");
    mmio_write32(MBOX_WRITE, (value & ~0xF) | (channel & 0xF));
}

/* Wait until mailbox is not empty, read, return value for our channel */
static uint32_t mbox_read(uint8_t channel)
{
    for (;;) {
        while (mmio_read32(MBOX_STATUS) & MBOX_EMPTY)
            __asm__ volatile ("nop");
        uint32_t val = mmio_read32(MBOX_READ);
        if ((val & 0xF) == channel)
            return val & ~0xF;
    }
}

